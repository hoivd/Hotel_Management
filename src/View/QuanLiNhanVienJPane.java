    private void Table_NhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_NhanVienMouseClicked
        ListSelectionModel listTable_NhanVien = Table_NhanVien.getSelectionModel();
        listTable_NhanVien.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        Button_ThemNhanVien.setEnabled(false);
        Button_XoaNhanVien.setEnabled(true);
        Button_SuaNhanVien.setEnabled(true);
        
        int indexTB = Table_NhanVien.getSelectedRow();
        if (indexTB < tblNhanVien.getRowCount() && indexTB >= 0){
            Text_MaNhanVien.setText(tblNhanVien.getValueAt(indexTB, 1).toString());
            Text_HoTen.setText(tblNhanVien.getValueAt(indexTB, 2).toString());
            Text_CCCD.setText(tblNhanVien.getValueAt(indexTB, 3).toString());
            try {
                JDateChooser_NgaySinh.setDate(new SimpleDateFormat("dd-MM-yyyy").parse(tblNhanVien.getValueAt(indexTB, 4).toString()));
            } catch (Exception e) {
                e.printStackTrace();
            }
            Text_SDT.setText(tblNhanVien.getValueAt(indexTB, 5).toString());
            ComboBox_GioiTinh.setSelectedItem(tblNhanVien.getValueAt(indexTB, 6));
            try {
                JDateChooser_NgayVaoLam.setDate(new SimpleDateFormat("dd-MM-yyyy").parse(tblNhanVien.getValueAt(indexTB, 7).toString()));
            } catch (Exception e) {
               e.printStackTrace();
            }
            ComboBox_ChucVu.setSelectedItem(tblNhanVien.getValueAt(indexTB, 8));
        }
    }//GEN-LAST:event_Table_NhanVienMouseClicked

    public void showKetQuaTimKiemNV(NhanVien KQTKNhanVien){
        listNhanVien = new NhanVienDAO().TimKiemNhanVien(KQTKNhanVien);
        tblNhanVien.setRowCount(0);
        int i = 1;
        for(NhanVien data : listNhanVien){
            tblNhanVien.addRow(new Object[]{i++, data.getMaNV(), data.getTenNV(), data.getCCCD(), new SimpleDateFormat("dd-MM-yyyy").format(data.getNgaySinh()),
                data.getSDT(), data.getGioiTinh(), new SimpleDateFormat("dd-MM-yyyy").format(data.getNgayVaoLam()), data.getChucVu()});
        }
        if(listNhanVien.size() <= 0){
             JOptionPane.showMessageDialog(this,"Không tìm thấy thông tin nhân viên. Vui lòng kiểm tra lại!");
        }
    }
    
