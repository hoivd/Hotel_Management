    public void clearText(){
        Text_TenTK.setText("");
        Text_MK.setText("");
        Text_XNMK.setText("");
        ComboBox_QuyenTK.setSelectedIndex(0);
    }
    
    public void showComboBox_MaNV(){
        ArrayList<NhanVien> maNV = new NhanVienDAO().getListNhanVien();
        for(NhanVien MaNV : maNV){
            ComboBox_MaNV.addItem(String.valueOf(MaNV.getMaNV()));
        }
    }
    
    public boolean check(){
        if(Text_TenTK.getText().equals("") || Text_MK.getText().equals("") || Text_XNMK.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin tài khoản.");
            return false;
        }
        if(!Text_MK.getText().equals(Text_XNMK.getText())){
            JOptionPane.showMessageDialog(this, "Mật khẩu không giống nhau.\nVui lòng nhập lại.");
            return false;
        }
        return true;
    }
    
