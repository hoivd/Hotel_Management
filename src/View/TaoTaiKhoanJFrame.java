    private void Button_DangKiTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_DangKiTKActionPerformed
        if(check()){
            User u = new User();
            u.setMaNV(Integer.parseInt(ComboBox_MaNV.getSelectedItem().toString()));
            u.setUsername(Text_TenTK.getText());
            u.setPassword(Text_MK.getText());
            u.setRole(ComboBox_QuyenTK.getSelectedItem().toString());
            if(new NhanVienDAO().DangKiTaiKhoan(u)){
                JOptionPane.showMessageDialog(this, "Đăng kí thành công.");
                clearText();
            } else {
                JOptionPane.showMessageDialog(this, "Đăng kí thất bại.");
                clearText();
            }
        }
    }//GEN-LAST:event_Button_DangKiTKActionPerformed

    private void ComboBox_MaNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboBox_MaNVActionPerformed
        NhanVien nv = new NhanVienDAO().TimKiemNhanVienBangMaNV(Integer.parseInt(ComboBox_MaNV.getSelectedItem().toString()));
        Text_HoTen.setText(nv.getTenNV());
        Text_CCCD.setText(nv.getCCCD());
        Text_ChucVu.setText(nv.getChucVu());
    }//GEN-LAST:event_ComboBox_MaNVActionPerformed

